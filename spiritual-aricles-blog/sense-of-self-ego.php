<h2>   <center>  Sense of Self: Ego  </center>  </h2>
<p>The Ego can be defined most accurately as your sense of self. The notion of &#8220;I&#8221; or &#8220;Me&#8221;. The idea that there is a &#8220;you&#8221; that is distinct and separate from everything else. The majority of people in the world live with a sense of self, with an ego, however that doesn&#8217;t validate it in any sense. Hinduism and Buddhism both fervently assert that there is no separately existing self and that those who continue to believe that there is will only continue to experience suffering. So how can we say that the Self does not exist? How can there be no ego and what would happen if one eliminated it?</p>
<p>The self does not exist for many reasons. Everything is interconnected and interdependent. For example, a Ship is made of many different things such as wood, metal, notion&#8230;.. The wood that made that ship came from a tree, which came from the dirt in the ground that the Sun helped nourish and grow. Nothing can exist on its own; it is all connected in some way or another. The Ship does not exist as a separate and distinct entity, it is made up of many other things and therefore cannot exist independently from the tree or the grass or the dirt.</p>
<p>It is merely an accumulation of everything else. It&#8217;s like a Candle and it&#8217;s Wax. You can melt the candle and shape its wax into many different things, but its essence never changes, it is still the Candle. All those different shapes you made are still just the Candle, they may look different, but they are not different. They are of the same nature of the Candle. The Self is similar; it exists in a similar fashion as the ship. It cannot exist independently or separately from everything else, simply because everything is dependant and connected with each other.</p>
<p>For this reason, Buddhism and Hinduism both assert that the self does not exist, so the common notion of an &#8220;I&#8221; or &#8220;Me&#8221; that is separate and different from everything else is completely false. There cannot be any &#8220;I&#8221; or &#8220;Me&#8221; that exists separately. The problem is that we, out of our ignorance, continue to believe that this separative self does exist. By doing this we create separation and pretend that we are separate and different from everything else, when in fact we are one and the same as everything else. Our ego is only a servant for our suffering. When we have this false notion of a separate self we become very involved in it. We become attached to it and play many unnecessary games with it.</p>
<p>And for what? For something that doesn&#8217;t even exist! How absurd! How horrible it is to become so involved and caught up in something that does not even exist. So you see, the ego has no positive function, it is a completely false notion that keeps us from understanding the Real Situation and keeps us from existing in Reality. Only when you eliminate your ego you can see that you already One with everything and always have been.</p>
<p>You can see that you and the Godhead are one and the same and always have been. You can see your true nature as everything and anything whatsoever, arising naturally as your own dynamic energy; your own creative play. You are open, without any boundaries, since everything is nothing other than You, you are Warm and Compassionate towards all. You become the Universe and exist lovingly with everything else. Moreover, you are liberated from the cycle of life, having seen that there is no &#8220;I&#8221;, you realize that there is no need to become so attached and involved in this &#8220;I&#8221;. Having realized this through direct experience, your ego will slowly dissolve and your whole experience will change. But the only way we can start dissolving our egos is to practice. We must meditate and we must find a Guru to guide us.</p>
<p>Many of us will read these things and go over it in our heads thinking, &#8220;Oh yeah, ego is bad. Right, right. I got it.&#8221; And then we just go back to our usual ego-tripping. It doesn&#8217;t work like that. We must practice and find the guidance of a realized Guru. It isn&#8217;t about reading; it&#8217;s about practicing and doing.</p>
