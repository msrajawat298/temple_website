<h2>   <center>Higher Calling (Chapter II: Bhagavad Geetha) </center>  </h2>
<p>Arjuna grieves over the prospect of ending the great lives of Bishma and Drona. The Lord eases Arjuna into submission so that He may identify the cause of Arjuna&#8217;s problem, just as a doctor seeks to treat his patient. Arjuna suffers because of his egocentric ideals, which create his delusional mindset. He sees the world and its beings solely on a physical level but cannot attain a spiritual understanding until he sheds this ego. Krishna seeks to spark an epiphany in Arjuna by explaining the real situation at hand. The Lord urges Arjuna to see beyond the physical being of his beloved Bishma and Drona and realize that Arjuna&#8217;s action will not disturb the &#8220;life principle&#8221; or soul of these blessed individuals. Krishna seeks to quickly revitalize Arjuna&#8217;s spirit by bringing him out of the physical world and, in essence, ending his mental anguish. Krishna thus interprets the finite nature of all the objects of the world and emphasizes a higher understanding.</p>
<p>Arjuna questions the Lord and wonders whether a person&#8217;s indifference will only lead to stoicism. However, the Lord responds by defining what is Real and Unreal in the world. Man must attain a mental equanimity to attain immortality. However, this level headedness should arise not from ignorance but from insight. We must live this life with the recognition that we will eventually throw away this physical form and eventually seek another body. While we may live with many ephemeral experiences, the Eternal Factor exists beyond the sense organs and the material world. After this discussion, the Lord&#8217;s aim is to explain the technique of living through Karma Yoga.</p>
<p>The fundamental belief of this practice is that &#8220;single-pointed&#8221; Karma without desires for material sensation results in a purification of the soul as well as a Spiritual awakening. This chapter provides extensive analysis of various rituals that may be performed to achieve this goal. However, Krishna cautions Arjuna, the common man, when he explains that the practice must be completed to the fullest and with a clean mind for it to have a positive effect. Arjuna is still unable to accept the Lord&#8217;s words. Therefore, Krishna elaborates on the idea when he exclaims that a &#8220;Wise Man&#8221; who has developed an evenness of the mind can reject ego-motivated actions. Thus, he alleviates the anxiety of constant desire and achieves &#8220;vasana-purgation&#8221;.</p>
<p>Vasanas or mental impression causes agitation in an individual because the person is caught in the material world. The &#8220;ladder of fall&#8221; connects attachment to inherent evil in man. The progression follows in this order: attachment leads to &#8220;desire&#8221;, which in turn leads to &#8220;anger&#8221; then &#8220;delusion&#8221; then &#8220;destruction of discrimination&#8221; and finally &#8220;death&#8221;. Therefore, even-mindedness is a necessary therapy.</p>
<p>The result of steady wisdom is the realization of an eternal happiness rather than the ephemeral bliss associated with material wealth. One does not have to destroy his capacity for perception but ensure that he does lose focus in the flood in sensual perception. Here lies the great difficulty. One must remain unmoved in times of great tragedy like a death in the family because a change in attitude anticipates the rebirth of one&#8217;s ego.<br />
Gaining this understanding requires wisdom the essence of this yoga and cannot be achieved in an instant. In our present day, we witness the exponential growth in the number of desires we might entertain. Technology has supposedly bettered our lives, but I believe the more &#8220;wealth&#8221; we have the more we divert our focus from the true goal. Krishna paints a clear picture. There is no difficulty in identifying the goal or the path. However, we face a great challenge in embracing a new perspective to meet that higher calling.</p>
